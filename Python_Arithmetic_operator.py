# Python Operators
# Operators are used to perform operations on variables and values.
# In the example below, we use the + operator to add together two values

print(10 + 5)  # Adds 10 and 5, Output: 15

# Python Arithmetic Operators

x = 5
y = 3

print(x + y)  # Addition: Adds x and y, Output: 8
print(x - y)  # Subtraction: Subtracts y from x, Output: 2
print(x * y)  # Multiplication: Multiplies x and y, Output: 15
print(x / y)  # Division: Divides x by y, Output: 1.6666666666666667
print(x % y)  # Modulus: Returns the remainder of x divided by y, Output: 2
print(x ** y)  # Exponentiation: Raises x to the power of y (x^y), Output: 125
print(x // y)  # Floor Division: Divides x by y and truncates the result to an integer, Output: 1