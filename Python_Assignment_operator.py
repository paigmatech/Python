##Python Assignment Operators
x = 5

x += 3

print(x)
# Outputs: 8
# In the example above, we used the += operator to add 3 to x. The result is stored in x.
# The same can be done with other arithmetic operators:
# -=, *=, /=, %=, **=, //=, &=, |=, ^=, >>=, <<=

# Example:
x = 5
y = 3

x -= y  # Subtracts y from x and assigns the result to x
print(x)  # Outputs: 2
x *= y  # Multiplies x by y and assigns the result to x
print(x)  # Outputs: 6
x /= y  # Divides x by y and assigns the result to x
print(x)  # Outputs: 2.0
x %= y  # Modulus: Returns the remainder of x divided by y and assigns the result to x
print(x)  # Outputs: 2.0
x **= y  # Exponentiation: Raises x to the power of y and assigns the result to x
print(x)  # Outputs: 8.0
x //= y  # Floor Division: Divides x by y and assigns the result to x
print(x)  # Outputs: 2.0
